from aiogram import Router, F
from aiogram.types import Message

help_router = Router()

HELP_TEXT = """Привет! Я <b>БотИК</b>. Вот что я умею:

🎲 <b>Случайный активист:</b>
<code>!кто</code> [текст] — выясняет, кто в чате [текст]
<code>!сантехник дня</code> — выбирает лучшего сантехника (работает один раз в сутки)

👤 <b>Информация об активисте:</b>
<code>!инфо</code> [@тег | фамилия] — показывает анкету активиста по тегу или фамилии

💬 <b>Цитаты:</b>
<code>!цитата</code> — ответь на сообщение или перешли сообщение (можно несколько), чтобы сохранить цитату
<code>!мудрость</code> — выдаёт случайную цитату из архива

🎴 <b>Таро:</b>
<code>!расклад</code> — карта дня и прогноз (одна карта на весь день, назавтра новая)

✨ <b>Разное:</b>
<code>!вероятность</code> [событие] — высчитывает вероятность предложенного события
<code>!подскажи</code> — помогает определиться в важных решениях
<code>!помощь</code> — вызывает это сообщение"""


@help_router.message(F.text == "!помощь")
async def help_cmd(message: Message):
    await message.answer(HELP_TEXT, parse_mode="HTML")
